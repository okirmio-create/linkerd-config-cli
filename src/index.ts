// ─── init <type> ─────────────────────────────────────────────────────────────

program
  .command("init <type>")
  .description("Generate Linkerd install config: default | ha | multi-cluster")
  .option("--cluster-domain <domain>", "Cluster domain", "cluster.local")
  .option("--proxy-cpu-request <cpu>", "Proxy CPU request", "100m")
  .option("--proxy-memory-request <mem>", "Proxy memory request", "20Mi")
  .option("--proxy-cpu-limit <cpu>", "Proxy CPU limit", "1000m")
  .option("--proxy-memory-limit <mem>", "Proxy memory limit", "250Mi")
  .option("--opaque-ports <ports>", "Opaque ports (comma-separated)", "25,587,3306,4444,5432,6379,9300,11211")
  .option("--skip-inbound-ports <ports>", "Skip inbound ports", "")
  .option("--skip-outbound-ports <ports>", "Skip outbound ports", "")
  .action((type, opts) => {
    const t = type.toLowerCase();
    if (!["default", "ha", "multi-cluster"].includes(t)) {
      console.error(chalk.red(`Unknown type: ${type}. Use: default | ha | multi-cluster`));
      process.exit(1);
    }

    const isHA = t === "ha";
    const isMulti = t === "multi-cluster";
    const replicas = isHA || isMulti ? 3 : 1;

    const skipInbound = opts.skipInboundPorts ? `\n  skipInboundPorts: "${opts.skipInboundPorts}"` : "";
    const skipOutbound = opts.skipOutboundPorts ? `\n  skipOutboundPorts: "${opts.skipOutboundPorts}"` : "";

    const multiclusterSection = isMulti ? `
# Multi-cluster gateway
gateway:
  enabled: true
  replicas: ${replicas}
  serviceAnnotations: {}

# Link configuration (run: linkerd multicluster install | kubectl apply -f -)
multicluster:
  enabled: true
` : "";

    const manifest = `# Linkerd Install Config — ${t.toUpperCase()}
# Generated by linkerd-config-cli v1.0.0
# Apply: linkerd install --values values.yaml | kubectl apply -f -

clusterDomain: ${opts.clusterDomain}

# Identity & trust anchor rotation
identity:
  issuer:
    scheme: kubernetes.io/tls
    tls:
      crtPEM: |
        # Replace with issuer certificate (PEM)
      keyPEM: |
        # Replace with issuer key (PEM)
  trustAnchorsPEM: |
    # Replace with trust anchor certificate (PEM)
    # Rotate with: linkerd upgrade --identity-trust-anchors-file new-anchor.pem
  trustDomain: ${opts.clusterDomain}

# Proxy resources & configuration
proxy:
  resources:
    cpu:
      request: "${opts.proxyCpuRequest}"
      limit: "${opts.proxyCpuLimit}"
    memory:
      request: "${opts.proxyMemoryRequest}"
      limit: "${opts.proxyMemoryLimit}"
  logLevel: warn,linkerd=info
  logFormat: plain
  opaquePorts: "${opts.opaquePorts}"${skipInbound}${skipOutbound}
  # Circuit breaking via timeout + retry budgets configured in ServiceProfile
  # Tap policy: enable/disable request inspection
  enableExternalProfiles: true

# Control plane HA
controllerReplicas: ${replicas}
enablePodAntiAffinity: ${isHA || isMulti}
${isHA || isMulti ? `
# HA: webhook replicas
webhookFailurePolicy: Fail
proxyInjector:
  replicas: ${replicas}
profileValidator:
  replicas: ${replicas}
policyValidator:
  replicas: ${replicas}
` : ""}
# CNI (set true to avoid init containers)
cniEnabled: false

# Tracing
tracing:
  enabled: false
${multiclusterSection}`;

    console.log(chalk.green(`# Linkerd ${t} config\n`));
    console.log(manifest);
    console.log(chalk.yellow("# Apply: linkerd install --values values.yaml | kubectl apply -f -"));
  });

// ─── add-service-profile <name> ──────────────────────────────────────────────

program
  .command("add-service-profile <name>")
  .description("Generate ServiceProfile with routes, retries, and timeouts")
  .option("--namespace <ns>", "Namespace", "default")
  .option("--timeout <ms>", "Default route timeout", "10000ms")
  .option("--retries", "Enable retries on GET routes", false)
  .option("--retry-budget-ratio <ratio>", "Retry budget ratio", "0.2")
  .option("--retry-budget-min <rps>", "Retry budget minimum RPS", "10")
  .action((name, opts) => {
    const retryBudget = `
  retryBudget:
    retryRatio: ${opts.retryBudgetRatio}
    minRetriesPerSecond: ${parseInt(opts.retryBudgetMin)}
    ttl: 10s`;

    const isRetryable = opts.retries ? "\n      isRetryable: true" : "";

    const manifest = `apiVersion: linkerd.io/v1alpha2
kind: ServiceProfile
metadata:
  name: ${name}.${opts.namespace}.svc.cluster.local
  namespace: ${opts.namespace}
spec:${retryBudget}
  routes:
    - name: GET /healthz
      condition:
        method: GET
        pathRegex: /healthz(/.*)?
      timeout: 5000ms${opts.retries ? "\n      isRetryable: true" : ""}

    - name: GET /api/v1/.*
      condition:
        method: GET
        pathRegex: /api/v1/.*
      timeout: ${opts.timeout}${isRetryable}

    - name: POST /api/v1/.*
      condition:
        method: POST
        pathRegex: /api/v1/.*
      timeout: ${opts.timeout}
      # POST mutations are not retried by default

    - name: PUT /api/v1/.*
      condition:
        method: PUT
        pathRegex: /api/v1/.*
      timeout: ${opts.timeout}

    - name: DELETE /api/v1/.*
      condition:
        method: DELETE
        pathRegex: /api/v1/.*
      timeout: ${opts.timeout}
`;

    console.log(chalk.green(`# ServiceProfile: ${name}\n`));
    console.log(manifest);
    console.log(chalk.yellow(`# Apply: kubectl apply -f ${name}-serviceprofile.yaml`));
  });

// ─── add-traffic-split <name> ────────────────────────────────────────────────

program
  .command("add-traffic-split <name>")
  .description("Generate TrafficSplit for canary deployments")
  .option("--namespace <ns>", "Namespace", "default")
  .option("--stable-weight <w>", "Stable backend weight", "900")
  .option("--canary-weight <w>", "Canary backend weight", "100")
  .option("--stable-service <svc>", "Stable service name", "")
  .option("--canary-service <svc>", "Canary service name", "")
  .action((name, opts) => {
    const stableSvc = opts.stableService || `${name}-stable`;
    const canarySvc = opts.canaryService || `${name}-canary`;

    const manifest = `# TrafficSplit — canary deployment for: ${name}
# SMI TrafficSplit v1alpha2
apiVersion: split.smi-spec.io/v1alpha2
kind: TrafficSplit
metadata:
  name: ${name}
  namespace: ${opts.namespace}
spec:
  # Root service — clients call this
  service: ${name}
  backends:
    - service: ${stableSvc}
      weight: ${opts.stableWeight}m
    - service: ${canarySvc}
      weight: ${opts.canaryWeight}m

---
# Stable service (points to current production pods)
apiVersion: v1
kind: Service
metadata:
  name: ${stableSvc}
  namespace: ${opts.namespace}
spec:
  selector:
    app: ${name}
    version: stable
  ports:
    - port: 80
      targetPort: 8080

---
# Canary service (points to new version pods)
apiVersion: v1
kind: Service
metadata:
  name: ${canarySvc}
  namespace: ${opts.namespace}
spec:
  selector:
    app: ${name}
    version: canary
  ports:
    - port: 80
      targetPort: 8080

# Rollout strategy:
# 1. Start: stable=900m canary=100m  (~10% traffic to canary)
# 2. Monitor: linkerd viz stat deploy -n ${opts.namespace}
# 3. Progress: increase canary weight, decrease stable weight
# 4. Complete: set canary=1000m stable=0m, then remove TrafficSplit
# 5. Rollback: set canary=0m stable=1000m immediately
`;

    console.log(chalk.green(`# TrafficSplit: ${name}\n`));
    console.log(manifest);
    console.log(chalk.yellow(`# Apply: kubectl apply -f ${name}-trafficsplit.yaml`));
  });

