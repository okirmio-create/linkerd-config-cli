// ─── validate [file] ─────────────────────────────────────────────────────────

program
  .command("validate [file]")
  .description("Validate Linkerd YAML manifests")
  .option("--strict", "Fail on warnings too", false)
  .action((file, opts) => {
    const LINKERD_KINDS = [
      "ServiceProfile",
      "TrafficSplit",
      "Server",
      "ServerAuthorization",
      "MeshTLSAuthentication",
      "AuthorizationPolicy",
      "Link",
    ];

    const LINKERD_API_GROUPS = [
      "linkerd.io",
      "policy.linkerd.io",
      "split.smi-spec.io",
      "multicluster.linkerd.io",
    ];

    if (!file) {
      console.log(chalk.yellow("# No file specified — showing validation rules\n"));
      console.log(`Linkerd manifest validation checks:

${chalk.cyan("Required fields:")}
  - apiVersion  (must match a Linkerd API group)
  - kind        (must be a known Linkerd resource type)
  - metadata.name
  - metadata.namespace
  - spec

${chalk.cyan("Supported API groups:")}
${LINKERD_API_GROUPS.map(g => `  - ${g}`).join("\n")}

${chalk.cyan("Supported resource kinds:")}
${LINKERD_KINDS.map(k => `  - ${k}`).join("\n")}

${chalk.cyan("ServiceProfile checks:")}
  - spec.routes must be an array
  - Each route must have: name, condition.method, condition.pathRegex
  - timeout format: <number>ms (e.g. 5000ms)
  - retryBudget: retryRatio 0-1, minRetriesPerSecond > 0, ttl format

${chalk.cyan("TrafficSplit checks:")}
  - spec.service must be set
  - spec.backends must have >= 2 entries
  - weights use millicores (e.g. 900m) — sum should equal 1000m

${chalk.cyan("Server checks:")}
  - spec.podSelector or spec.externalWorkloadSelector required
  - spec.port required
  - proxyProtocol: HTTP/1 | HTTP/2 | gRPC | opaque | TLS

${chalk.cyan("ServerAuthorization checks:")}
  - spec.server.name required
  - spec.client must have at least one of: meshTLS, unauthenticated

${chalk.cyan("Common issues:")}
  - ServiceProfile name must match: <svc>.<namespace>.svc.<clusterDomain>
  - TrafficSplit in same namespace as root service
  - mTLS identity format: <sa>.<ns>.serviceaccount.identity.linkerd.<domain>

${chalk.cyan("CLI validation:")}
  linkerd check                    — control plane health
  linkerd check --proxy            — data plane health
  linkerd viz check                — viz extension health
  kubectl apply --dry-run=server   — server-side dry run`);
      return;
    }

    if (!existsSync(file)) {
      console.error(chalk.red(`File not found: ${file}`));
      process.exit(1);
    }

    const content = readFileSync(file, "utf-8");
    const errors: string[] = [];
    const warnings: string[] = [];
    const infos: string[] = [];

    // Split multi-doc YAML
    const docs = content.split(/^---\s*$/m).filter(d => d.trim().length > 0);
    let docIndex = 0;

    for (const doc of docs) {
      docIndex++;
      const prefix = docs.length > 1 ? `[doc ${docIndex}] ` : "";

      // Basic field checks via regex (no full YAML parser dep)
      const hasApiVersion = /^\s*apiVersion\s*:/m.test(doc);
      const hasKind = /^\s*kind\s*:/m.test(doc);
      const hasName = /^\s*name\s*:/m.test(doc);
      const hasNamespace = /^\s*namespace\s*:/m.test(doc);
      const hasSpec = /^\s*spec\s*:/m.test(doc);

      const apiVersionMatch = doc.match(/^\s*apiVersion\s*:\s*(.+)$/m);
      const kindMatch = doc.match(/^\s*kind\s*:\s*(.+)$/m);
      const apiVersion = apiVersionMatch ? apiVersionMatch[1].trim() : "";
      const kind = kindMatch ? kindMatch[1].trim() : "";

      if (!hasApiVersion) errors.push(`${prefix}Missing: apiVersion`);
      if (!hasKind) errors.push(`${prefix}Missing: kind`);
      if (!hasName) errors.push(`${prefix}Missing: metadata.name`);
      if (!hasNamespace) warnings.push(`${prefix}Missing: metadata.namespace (recommended)`);
      if (!hasSpec && kind !== "Namespace") errors.push(`${prefix}Missing: spec`);

      if (apiVersion && !LINKERD_API_GROUPS.some(g => apiVersion.startsWith(g))) {
        if (apiVersion !== "v1" && !apiVersion.startsWith("apps/") && !apiVersion.startsWith("networking.k8s.io")) {
          warnings.push(`${prefix}apiVersion "${apiVersion}" is not a known Linkerd API group`);
        }
      }

      if (kind && LINKERD_KINDS.includes(kind)) {
        infos.push(`${prefix}Found Linkerd resource: ${kind}`);

        if (kind === "ServiceProfile") {
          if (!/spec:\s*\n(\s+\w.*\n)*\s+routes:/m.test(doc)) {
            warnings.push(`${prefix}ServiceProfile: spec.routes not found`);
          }
          // Check name format
          const nameMatch = doc.match(/^\s*name\s*:\s*(.+)$/m);
          if (nameMatch && !nameMatch[1].includes(".svc.")) {
            warnings.push(`${prefix}ServiceProfile name should match: <svc>.<ns>.svc.cluster.local`);
          }
        }

        if (kind === "TrafficSplit") {
          if (!/backends:/m.test(doc)) {
            errors.push(`${prefix}TrafficSplit: spec.backends required`);
          }
          if (!/service:/m.test(doc)) {
            errors.push(`${prefix}TrafficSplit: spec.service (root service) required`);
          }
        }

        if (kind === "Server") {
          if (!/port:/m.test(doc)) {
            errors.push(`${prefix}Server: spec.port required`);
          }
          if (!/podSelector:|externalWorkloadSelector:/m.test(doc)) {
            errors.push(`${prefix}Server: spec.podSelector or spec.externalWorkloadSelector required`);
          }
        }

        if (kind === "ServerAuthorization") {
          if (!/meshTLS:|unauthenticated:/m.test(doc)) {
            errors.push(`${prefix}ServerAuthorization: spec.client must specify meshTLS or unauthenticated`);
          }
        }
      }
    }

    console.log(chalk.green(`# Validating: ${file} (${docs.length} document${docs.length !== 1 ? "s" : ""})\n`));

    if (infos.length > 0) {
      console.log(chalk.cyan("Resources found:"));
      infos.forEach(i => console.log(chalk.cyan(`  ✓ ${i}`)));
      console.log();
    }

    if (errors.length === 0 && warnings.length === 0) {
      console.log(chalk.green("✓ No issues found"));
    }

    if (warnings.length > 0) {
      console.log(chalk.yellow("Warnings:"));
      warnings.forEach(w => console.log(chalk.yellow(`  ⚠ ${w}`)));
      console.log();
    }

    if (errors.length > 0) {
      console.log(chalk.red("Errors:"));
      errors.forEach(e => console.log(chalk.red(`  ✗ ${e}`)));
      console.log();
    }

    const hasIssues = errors.length > 0 || (opts.strict && warnings.length > 0);
    if (hasIssues) {
      console.log(chalk.red(`Validation failed: ${errors.length} error(s), ${warnings.length} warning(s)`));
      console.log(chalk.yellow("\n# Also run: kubectl apply --dry-run=server -f " + file));
      process.exit(1);
    } else {
      console.log(chalk.green(`Validation passed (${errors.length} errors, ${warnings.length} warnings)`));
      console.log(chalk.yellow("\n# Also run: linkerd check && kubectl apply --dry-run=server -f " + file));
    }
  });

